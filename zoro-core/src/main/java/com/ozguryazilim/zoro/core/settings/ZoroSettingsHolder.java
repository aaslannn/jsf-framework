package com.ozguryazilim.zoro.core.settings;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Table;

import com.ozguryazilim.zoro.core.db.entity.BaseEntity;

@Table(name = "SETTINGS_HOLDER")
@Entity
public class ZoroSettingsHolder extends BaseEntity {
	private static final long	serialVersionUID	= 7873607931915938342L;

	@Column
	private int					guiListSize;

	@Column
	private String				ldapHost;

	@Column
	private int					ldapPort;

	@Column
	private String				ldapUser;

	@Column
	private String				ldapPass;

	@Column
	private String				ldapSearchDN;

	@Column
	private String				ldapLoginAttr;

	@Column
	private String				adminUsername;

	/**
	 * @return the guiListSize
	 */
	public int getGuiListSize() {
		return guiListSize;
	}

	/**
	 * @param guiListSize
	 *            the guiListSize to set
	 */
	public void setGuiListSize(int guiListSize) {
		this.guiListSize = guiListSize;
	}

	/**
	 * @return the ldapHost
	 */
	public String getLdapHost() {
		return ldapHost;
	}

	/**
	 * @param ldapHost
	 *            the ldapHost to set
	 */
	public void setLdapHost(String ldapHost) {
		this.ldapHost = ldapHost;
	}

	/**
	 * @return the ldapPort
	 */
	public int getLdapPort() {
		return ldapPort;
	}

	/**
	 * @param ldapPort
	 *            the ldapPort to set
	 */
	public void setLdapPort(int ldapPort) {
		this.ldapPort = ldapPort;
	}

	/**
	 * @return the ldapUser
	 */
	public String getLdapUser() {
		return ldapUser;
	}

	/**
	 * @param ldapUser
	 *            the ldapUser to set
	 */
	public void setLdapUser(String ldapUser) {
		this.ldapUser = ldapUser;
	}

	/**
	 * @return the ldapPass
	 */
	public String getLdapPass() {
		return ldapPass;
	}

	/**
	 * @param ldapPass
	 *            the ldapPass to set
	 */
	public void setLdapPass(String ldapPass) {
		this.ldapPass = ldapPass;
	}

	/**
	 * @return the ldapSearchDN
	 */
	public String getLdapSearchDN() {
		return ldapSearchDN;
	}

	/**
	 * @param ldapSearchDN
	 *            the ldapSearchDN to set
	 */
	public void setLdapSearchDN(String ldapSearchDN) {
		this.ldapSearchDN = ldapSearchDN;
	}

	/**
	 * @return the ldapLoginAttr
	 */
	public String getLdapLoginAttr() {
		return ldapLoginAttr;
	}

	/**
	 * @param ldapLoginAttr
	 *            the ldapLoginAttr to set
	 */
	public void setLdapLoginAttr(String ldapLoginAttr) {
		this.ldapLoginAttr = ldapLoginAttr;
	}

	/**
	 * @return the adminUsername
	 */
	public String getAdminUsername() {
		return adminUsername;
	}

	/**
	 * @param adminUsername
	 *            the adminUsername to set
	 */
	public void setAdminUsername(String adminUsername) {
		this.adminUsername = adminUsername;
	}

}
